#Exercise 2.7 this program determines if 1024 is a multiple of 4 and whether 2 is a multiple of 10

number_one = 1024
number_two = 2

if 1024 % 4 == 0:
    print(number_one, 'is a multiple of 4')
else:
    print(number_one, 'is not a multiple of 4')

if 2 % 10 == 0:
    print(number_two, 'is a multiple of 10')
else:
    print(number_two, 'is not a multiple of 10')
